<h3><a href= "#">Purchase of immovable property Applicability of Section 194IA</a></h3>
Section 194IA, a newly inserted section, mandates a person to deduct Tax at Source (TDS) at 1% while paying consideration for buying any Immovable property other than rural agricultural land in India to any resident seller/builder.<br>

The said provisions are applicable where the transaction value is more than Rs. 50 lakhs. Also, the given provisions have become a concern while buying and paying any amount for underdeveloped plot or under construction apartment/villa, etc., from builders. This amendment has taken effect from 1st June, 2013.<br>

Further in case if the builder does not provide Permanent Account Number (PAN) to the buyer, then the buyer has to deduct TDS at higher rate i.e. at the rate of 20%.<br>

